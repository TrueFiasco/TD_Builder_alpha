@app.call_tool()
async def call_tool(name: str, arguments: dict) -> Sequence[TextContent]:
    # Phase 6: New tools - td_validate, td_convert, td_build_network
    if name == "td_validate":
        try:
            network_json = arguments.get("network")
            if not network_json:
                return [TextContent(type="text", text="Error: 'network' is required")]

            format_layer = arguments.get("format_layer", "builder")
            verbose = arguments.get("verbose", False)

            # Convert to TDNetwork if needed
            if format_layer == "builder":
                network = converter.from_builder(network_json)
            elif format_layer == "canonical":
                network = converter.from_canonical(network_json)
            else:  # extended
                # TODO: Implement proper Extended JSON deserialization
                # For now, use builder format
                network = converter.from_builder(network_json)

            # Validate
            project_name = network_json.get("meta", {}).get("project_name", "network")
            report = validator.validate(network, project_name)

            # Build response
            result = {
                "valid": report.valid,
                "total_errors": report.total_errors,
                "total_warnings": report.total_warnings,
                "errors": [{"stage": e.stage, "message": e.message, "severity": e.severity}
                          for e in report.get_errors()],
                "warnings": [{"stage": w.stage, "message": w.message, "severity": w.severity}
                            for w in report.get_warnings()]
            }

            if verbose:
                # Build stages from report.stages list
                result["stages"] = {}
                for stage_report in report.stages:
                    result["stages"][stage_report.stage] = {
                        "passed": stage_report.status == "PASS",
                        "errors": len(stage_report.errors),
                        "warnings": len(stage_report.warnings)
                    }

            return [TextContent(type="text", text=json.dumps(result, indent=2))]
        except Exception as e:
            return [TextContent(type="text", text=f"Error validating network: {str(e)}")]

    elif name == "td_convert":
        try:
            network_json = arguments.get("network")
            if not network_json:
                return [TextContent(type="text", text="Error: 'network' is required")]

            source_layer = arguments.get("source_layer")
            target_layer = arguments.get("target_layer")

            if not source_layer or not target_layer:
                return [TextContent(type="text", text="Error: 'source_layer' and 'target_layer' are required")]

            # Convert source → Extended (ground truth)
            if source_layer == "builder":
                network = converter.from_builder(network_json)
            elif source_layer == "canonical":
                network = converter.from_canonical(network_json)
            else:  # already extended
                # TODO: Proper Extended deserialization
                network = converter.from_builder(network_json)

            # Convert Extended → target
            if target_layer == "builder":
                result_json = converter.to_builder(network)
            elif target_layer == "canonical":
                result_json = converter.to_canonical(network)
            else:  # extended
                # Return extended format (serialize TDNetwork to builder and back)
                # This preserves structure while giving a JSON-serializable dict
                result_json = converter.to_builder(network)
                result_json["format_layer"] = "extended"

            return [TextContent(type="text", text=json.dumps(result_json, indent=2))]
        except Exception as e:
            return [TextContent(type="text", text=f"Error converting network: {str(e)}")]

    elif name == "td_build_network":
        try:
            network_design = arguments.get("network_design")
            network_json = arguments.get("network")

            output_path = arguments.get("output_path")
            if not output_path:
                return [TextContent(type="text", text="Error: 'output_path' is required")]

            mode = arguments.get("mode", "toe")
            verbose = arguments.get("verbose", False)

            # Normalize path - if Linux-style path, redirect to Windows builds dir
            # Claude Desktop runs in Linux sandbox but MCP server is on Windows
            if output_path.startswith('/') or ':' not in output_path:
                # Extract just the filename and use Windows builds directory
                filename = Path(output_path).name
                output_path = f"C:/TD_Projects/builds/{filename}"

            # Ensure output directory exists
            output = Path(output_path)
            output.parent.mkdir(parents=True, exist_ok=True)

            # Check if using new network_design format with ToeBuilderBridge
            if network_design and HAS_TOE_BUILDER_BRIDGE:
                # Use tested ToeBuilderBridge with embed_tox support
                try:
                    if mode == "tox":
                        bridge = ToxBuilder(output.parent)
                        result_path = bridge.build_tox(network_design, output.stem)
                    else:
                        bridge = ToeBuilderBridge(output.parent)
                        result_path = bridge.build_from_design(network_design, output.stem)

                    # Count operators and connections (BUG-I FIX: include container connections)
                    op_count = 0
                    conn_count = len(network_design.get("connections", []))
                    for container in network_design.get("containers", []):
                        op_count += len(container.get("operators", []))
                        # Count container-internal connections (flat or nested format)
                        conn_count += len(container.get("connections", []))
                        network = container.get("network", {})
                        conn_count += len(network.get("connections", []))
                    op_count += len(network_design.get("operators", []))

                    # Check if file was actually created
                    if result_path is None or not Path(result_path).exists():
                        return [TextContent(type="text", text=json.dumps({
                            "success": False,
                            "builder": "ToeBuilderBridge",
                            "error": "Build completed but file was not created",
                            "attempted_path": str(result_path) if result_path else "None"
                        }, indent=2))]

                    # Read file and encode as base64 for transfer
                    file_data = None
                    file_size = Path(result_path).stat().st_size
                    if file_size < 10 * 1024 * 1024:  # Only transfer files under 10MB
                        with open(result_path, 'rb') as f:
                            file_data = base64.b64encode(f.read()).decode('utf-8')

                    result = {
                        "success": True,
                        "builder": "ToeBuilderBridge",
                        "output_file": str(result_path),
                        "file_size": file_size,
                        "operators": op_count,
                        "connections": conn_count,
                        "features_used": {
                            "containers": len(network_design.get("containers", [])) > 0,
                            "embed_tox": any(
                                op.get("embed_tox")
                                for container in network_design.get("containers", [])
                                for op in container.get("operators", [])
                            )
                        }
                    }

                    # Include file data if available
                    if file_data:
                        result["file_base64"] = file_data
                        result["transfer_note"] = "File included as base64. Decode and save to use."

                    return [TextContent(type="text", text=json.dumps(result, indent=2))]
                except Exception as e:
                    import traceback
                    return [TextContent(type="text", text=json.dumps({
                        "success": False,
                        "builder": "ToeBuilderBridge",
                        "error": str(e),
                        "traceback": traceback.format_exc()
                    }, indent=2))]

            elif network_design and not HAS_TOE_BUILDER_BRIDGE:
                return [TextContent(type="text", text=json.dumps({
                    "success": False,
                    "error": "network_design format requires ToeBuilderBridge which failed to import",
                    "import_error": TOE_BUILDER_BRIDGE_ERROR
                }, indent=2))]

            elif not network_json:
                return [TextContent(type="text", text="Error: either 'network' or 'network_design' is required")]

            # Convert network format to network_design and use ToeBuilderBridge
            # This handles: operators/nodes, parameters/params, combined types like noiseCHOP
            if HAS_TOE_BUILDER_BRIDGE:
                try:
                    # Convert network to network_design format
                    converted_design = {
                        "project": network_json.get("meta", {}).get("project_name", output.stem),
                        "operators": [],
                        "connections": network_json.get("connections", [])
                    }

                    # Handle both "operators" and "nodes" keys
                    ops = network_json.get("operators", []) or network_json.get("nodes", [])
                    for op in ops:
                        converted_op = {
                            "name": op.get("name", ""),
                            "type": op.get("type", ""),
                        }
                        # Handle both "parameters" and "params" keys
                        params = op.get("parameters", {}) or op.get("params", {})
                        if params:
                            converted_op["parameters"] = params
                        converted_design["operators"].append(converted_op)

                    # Use ToeBuilderBridge
                    if mode == "tox":
                        bridge = ToxBuilder(output.parent)
                        result_path = bridge.build_tox(converted_design, output.stem)
                    else:
                        bridge = ToeBuilderBridge(output.parent)
                        result_path = bridge.build_from_design(converted_design, output.stem)

                    # Check if file was actually created
                    if result_path is None or not Path(result_path).exists():
                        return [TextContent(type="text", text=json.dumps({
                            "success": False,
                            "builder": "ToeBuilderBridge",
                            "error": "Build completed but file was not created",
                            "attempted_path": str(result_path) if result_path else "None",
                            "converted_from": "network"
                        }, indent=2))]

                    # Return local file path directly (MCP runs locally, no need for base64)
                    file_size = Path(result_path).stat().st_size

                    # Verify file is valid (should be at least 500 bytes for a minimal .tox)
                    if file_size < 500:
                        return [TextContent(type="text", text=json.dumps({
                            "success": False,
                            "builder": "ToeBuilderBridge",
                            "error": f"Generated file too small ({file_size} bytes) - toecollapse may have failed",
                            "output_file": str(result_path),
                            "toc_file": str(result_path).replace('.tox', '.tox.toc').replace('.toe', '.toe.toc'),
                            "dir": str(result_path) + ".dir",
                            "hint": "Check if toecollapse.exe is available and working"
                        }, indent=2))]

                    result = {
                        "success": True,
                        "builder": "ToeBuilderBridge",
                        "output_file": str(result_path),
                        "file_size": file_size,
                        "operators": len(converted_design.get("operators", [])),
                        "connections": len(converted_design.get("connections", [])),
                        "converted_from": "network",
                        "note": "File created locally. Open directly in TouchDesigner."
                    }

                    return [TextContent(type="text", text=json.dumps(result, indent=2))]
                except Exception as e:
                    import traceback
                    return [TextContent(type="text", text=json.dumps({
                        "success": False,
                        "builder": "ToeBuilderBridge",
                        "error": str(e),
                        "traceback": traceback.format_exc()
                    }, indent=2))]

            # Fall back to original NetworkBuilder if ToeBuilderBridge unavailable
            meta = network_json.get("meta", {})
            project_name = meta.get("project_name", "network")

            builder = NetworkBuilder(project_name, mode=mode)

            # Add operators - handle both "operators" and "nodes" keys
            nodes = network_json.get("operators", []) or network_json.get("nodes", [])
            for node in nodes:
                node_path = node.get("path", "")
                name = node_path.split("/")[-1] if node_path else node.get("name", "")

                # Parse type - handle combined format like "noiseCHOP"
                op_type = node.get("type", "")
                family = node.get("family", "")
                if not family and op_type:
                    # Try to extract family from combined type (e.g., "noiseCHOP" -> "CHOP", "noise")
                    for fam in ["CHOP", "TOP", "SOP", "DAT", "COMP", "MAT", "POP"]:
                        if op_type.upper().endswith(fam):
                            family = fam
                            op_type = op_type[:-len(fam)]
                            break

                if not name or not family or not op_type:
                    continue

                # Determine parent
                parent = None
                if "/" in node_path:
                    parent = "/".join(node_path.split("/")[:-1])

                builder.add_operator(name, family, op_type, parent=parent)

                # Set parameters - handle both "parameters" and "params" keys
                params = node.get("parameters", {}) or node.get("params", {})
                for param_name, param_value in params.items():
                    try:
                        builder.set_parameter(name, param_name, param_value)
                    except Exception:
                        pass  # Skip invalid parameters

                # Set text content for DAT operators (Table DAT, Text DAT, Script DAT)
                text_content = node.get("text") or node.get("content")
                if text_content and family == "DAT":
                    try:
                        builder.set_text(name, text_content)
                    except Exception:
                        pass  # Skip if set_text fails

            # Add connections
            connections = network_json.get("connections", [])
            for conn in connections:
                from_name = conn.get("from", "")
                to_name = conn.get("to", "")
                to_input = conn.get("to_input", 0)

                if from_name and to_name:
                    try:
                        builder.connect(from_name, to_name, target_input=to_input)
                    except Exception:
                        pass  # Skip invalid connections

            # Validate
            if not builder.is_valid():
                report = builder.validate()
                errors = [e.message for e in report.get_errors()]
                return [TextContent(type="text", text=json.dumps({
                    "success": False,
                    "error": "Network validation failed",
                    "errors": errors[:5]
                }, indent=2))]

            # Build
            if mode == "toe":
                toc_file = builder.build_toe(output, verbose=verbose)
            else:
                toc_file = builder.build_tox(output, verbose=verbose)

            result = {
                "success": True,
                "builder": "NetworkBuilder",
                "toc_file": str(toc_file),
                "dir": str(toc_file).replace(".toc", ".dir"),
                "operators": len(builder),
                "connections": len(builder.connections),
                "next_step": f"toecollapse {toc_file}"
            }

            return [TextContent(type="text", text=json.dumps(result, indent=2))]
        except Exception as e:
            import traceback
            return [TextContent(type="text", text=json.dumps({
                "success": False,
                "error": str(e),
                "traceback": traceback.format_exc()
            }, indent=2))]

    elif name == "td_compact_expertise":
        if not HAS_COMPACTION:
            return [TextContent(type="text", text=json.dumps({
                "success": False,
                "error": "Compaction utilities unavailable",
                "details": COMPACTION_IMPORT_ERROR
            }, indent=2))]

        try:
            refresh_yaml = bool(arguments.get("refresh_yaml", True))
            events_path_arg = arguments.get("events_path")
            state_path_arg = arguments.get("state_path")

            from pathlib import Path as CompPath
            events_path = CompPath(events_path_arg) if events_path_arg else None
            state_path = CompPath(state_path_arg) if state_path_arg else None

            success, msg = compact_events_to_state(events_path=events_path, state_path=state_path)
            result = {
                "success": success,
                "compaction": msg
            }

            if refresh_yaml:
                refresh_success, refresh_msg = refresh_legacy_yaml(state_path=state_path)
                result["refresh_success"] = refresh_success
                result["refresh"] = refresh_msg

            return [TextContent(type="text", text=json.dumps(result, indent=2))]
        except Exception as e:
            import traceback
            return [TextContent(type="text", text=json.dumps({
                "success": False,
                "error": str(e),
                "traceback": traceback.format_exc()
            }, indent=2))]

    # Original td_assistant tool
    elif name == "td_assistant":
        action = arguments.get("action")

        if action == "stats":
            return [TextContent(type="text", text=json.dumps(retrieval.get_performance_stats(), indent=2))]

        if action == "build_python":
            network = arguments.get("network") or {}
            target_parent_path = arguments.get("target_parent_path") or "/project1"
            collision_policy = arguments.get("collision_policy") or "reuse"
            script = build_text_dat_script(network, target_parent_path, collision_policy=collision_policy)
            result = {"target_parent_path": target_parent_path, "script": script}
            return [TextContent(type="text", text=json.dumps(result, indent=2))]

        if action == "query":
            query = arguments.get("query")
            if not isinstance(query, str) or not query.strip():
                return [TextContent(type="text", text="Error: 'query' must be a non-empty string")]

            n_results = int(arguments.get("n_results", 5) or 5)
            filters = arguments.get("filters") or {}
            allowed_sources = set(_normalize_sources(filters.get("sources")))
            categories = set(_normalize_sources(filters.get("categories")))
            difficulty = filters.get("difficulty")
            min_popularity = filters.get("min_popularity")
            tiers = set(filters.get("tiers") or [])

            def passes_filters(chunk: Dict[str, Any]) -> bool:
                meta = chunk.get("meta", {}) or {}
                source = _chunk_source(chunk)
                if allowed_sources and source not in allowed_sources:
                    return False
                if difficulty and meta.get("difficulty_level") and meta.get("difficulty_level") != difficulty:
                    return False
                if min_popularity is not None:
                    try:
                        if float(meta.get("popularity_score", 0)) < float(min_popularity):
                            return False
                    except Exception:
                        return False
                if tiers:
                    try:
                        if int(meta.get("tier")) not in {int(t) for t in tiers}:
                            return False
                    except Exception:
                        return False
                if categories:
                    chunk_cats = meta.get("use_case_categories") or []
                    if isinstance(chunk_cats, str):
                        chunk_cats = [chunk_cats]
                    if not set([str(c) for c in chunk_cats]).intersection(categories):
                        return False
                return True

            # Overfetch then filter (retrieval itself is unfiltered)
            # Use async version for better performance
            raw = await retrieval.hybrid_search_async(query, n_results=max(25, n_results * 5))

            results: List[Dict[str, Any]] = []
            for chunk, score in raw:
                if not passes_filters(chunk):
                    continue
                source = _chunk_source(chunk)
                results.append(
                    {
                        "score": score,
                        "source": source,
                        "text": chunk.get("text", ""),
                        "meta": chunk.get("meta", {}),
                        "chunk_type": chunk.get("chunk_type"),
                    }
                )
                if len(results) >= n_results:
                    break

            payload = {
                "query": query,
                "n_results": n_results,
                "filters": {
                    **({"sources": sorted(list(allowed_sources))} if allowed_sources else {}),
                    **({"categories": sorted(list(categories))} if categories else {}),
                    **({"difficulty": difficulty} if difficulty else {}),
                    **({"min_popularity": min_popularity} if min_popularity is not None else {}),
                    **({"tiers": sorted(list(tiers))} if tiers else {}),
                },
                "results": results,
            }
            return [TextContent(type="text", text=json.dumps(payload, indent=2))]

        if action == "batch_query":
            # Run multiple queries in parallel for efficiency
            queries = arguments.get("queries", [])
            if not queries:
                return [TextContent(type="text", text=json.dumps({"error": "No queries provided"}))]

            n_results = int(arguments.get("n_results", 3))  # Fewer per query in batch mode

            async def run_query(q: str):
                try:
                    raw = await retrieval.hybrid_search_async(q, n_results=n_results * 3)
                    results = []
                    for chunk, score in raw[:n_results]:
                        results.append({
                            "score": score,
                            "source": _chunk_source(chunk),
                            "text": chunk.get("text", "")[:500],  # Truncate for batch
                            "meta": chunk.get("meta", {}),
                        })
                    return {"query": q, "results": results}
                except Exception as e:
                    return {"query": q, "error": str(e)}

            # Run all queries in parallel
            import asyncio
            batch_results = await asyncio.gather(*[run_query(q) for q in queries])

            return [TextContent(type="text", text=json.dumps({
                "batch_count": len(queries),
                "results": batch_results
            }, indent=2))]

        return [TextContent(type="text", text=f"Error: unknown action '{action}'")]

    # Unknown tool
    return [TextContent(type="text", text=f"Error: unknown tool '{name}'")]


async def main() -> None:
    async with stdio_server() as (read_stream, write_stream):
        await app.run(read_stream, write_stream, app.create_initialization_options())


if __name__ == "__main__":
    asyncio.run(main())
