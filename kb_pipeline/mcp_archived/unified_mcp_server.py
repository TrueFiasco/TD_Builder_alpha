def _normalize_sources(value: Any) -> List[str]:
    if value is None:
        return []
    if isinstance(value, str):
        return [value]
    if isinstance(value, list):
        return [str(v) for v in value]
    return [str(value)]


def _chunk_source(chunk: Dict[str, Any]) -> str:
    meta = chunk.get("meta", {}) or {}
    source = meta.get("source")
    if source:
        return str(source)
    # Infer when missing (some docs chunks may not have source populated)
    if meta.get("operator_type") or meta.get("relpath") or meta.get("has_curator_summary") is not None:
        return "snippets"
    if meta.get("palette_name") or meta.get("palette_category"):
        return "palette"
    return "docs"


def _safe_int(value: Any, default: int = 0) -> int:
    try:
        return int(value)
    except Exception:
        return default


def _td_create_type(td_type: str) -> str:
    """
    Convert a TD family/type like 'CHOP:noise' to 'noiseCHOP' for parent.create().
    If td_type is already a create type (no ':'), return as-is.
    """
    if ":" not in td_type:
        return td_type
    family, specific = td_type.split(":", 1)
    family = family.strip().upper()
    specific = specific.strip()
    if not specific:
        return td_type
    return f"{specific}{family}"


def build_text_dat_script(
    network: Dict[str, Any],
    target_parent_path: str,
    collision_policy: str = "reuse",
) -> str:
    """
    Generate a TouchDesigner Text DAT Python script to create a network.

    collision_policy:
      - reuse: reuse existing operator if name matches
      - delete: delete existing operator then recreate
    """
    # Accept both "nodes" and "operators" keys (Claude Desktop sends "operators")
    nodes = network.get("nodes", []) or network.get("operators", []) or []
    connections = network.get("connections", []) or []

    lines: List[str] = []
    lines.append("# TouchDesigner Text DAT builder script")
    lines.append("# Generated by Unified TD KB MCP Server")
    lines.append("#")
    lines.append("# How to run:")
    lines.append("# - Paste into a Text DAT")
    lines.append("# - Run the DAT (or call build())")
    lines.append("")
    lines.append("def build(target_parent_path=None):")
    lines.append(f"    parent_path = target_parent_path or {target_parent_path!r}")
    lines.append("    parent = op(parent_path)")
    lines.append("    if parent is None:")
    lines.append("        raise Exception(f'Parent not found: {parent_path}')")
    lines.append("")
    lines.append("    created = {}")
    lines.append("")

    # Create nodes
    lines.append("    # Create/reuse nodes")
    for node in nodes:
        name = node.get("name")
        td_type = node.get("type")
        if not name or not td_type:
            continue
        create_type = _td_create_type(str(td_type))
        lines.append(f"    existing = parent.op({name!r})")
        if collision_policy == "delete":
            lines.append("    if existing is not None:")
            lines.append("        try:")
            lines.append("            existing.destroy()")
            lines.append("        except Exception:")
            lines.append("            pass")
            lines.append("        existing = None")
        lines.append(f"    node = existing or parent.create({create_type!r}, {name!r})")
        lines.append(f"    created[{name!r}] = node")
        lines.append("")

    # Apply parameters
    lines.append("    # Apply parameters")
    for node in nodes:
        name = node.get("name")
        # Accept both "params" and "parameters" keys
        params = node.get("params") or node.get("parameters") or {}
        if not name or not isinstance(params, dict) or not params:
            continue
        lines.append(f"    node = created.get({name!r})")
        lines.append("    if node is None:")
        lines.append("        pass")
        for param_name, param_value in params.items():
            if not isinstance(param_name, str) or not param_name:
                continue
            # Use getattr to avoid syntax issues with invalid parameter names
            lines.append("    try:")
            lines.append(f"        p = getattr(node.par, {param_name!r}, None)")
            lines.append("        if p is not None:")
            lines.append(f"            p.val = {param_value!r}")
            lines.append("    except Exception:")
            lines.append("        pass")
        lines.append("")

    # Apply text content for DAT operators
    lines.append("    # Apply text content for DAT operators")
    for node in nodes:
        name = node.get("name")
        text_content = node.get("text") or node.get("content")
        td_type = str(node.get("type", "")).upper()
        if not name or not text_content:
            continue
        # Only apply text to DAT operators
        if "DAT" in td_type:
            lines.append(f"    node = created.get({name!r})")
            lines.append("    if node is not None:")
            lines.append(f"        node.text = {text_content!r}")
            lines.append("")

    # Wire connections
    lines.append("    # Wire connections")
    for conn in connections:
        from_name = conn.get("from")
        to_name = conn.get("to")
        to_input = conn.get("to_input", 0)
        if not from_name or not to_name:
            continue
        lines.append(f"    src = created.get({str(from_name)!r})")
        lines.append(f"    dst = created.get({str(to_name)!r})")
        lines.append("    if src is None or dst is None:")
        lines.append("        pass")
        lines.append("    else:")
        lines.append(f"        try: dst.setInput({_safe_int(to_input, 0)}, src)")
        lines.append("        except Exception: pass")
        lines.append("")

    lines.append("    return created")
    lines.append("")
    lines.append("build()")
    lines.append("")

    return "\n".join(lines)


